"""**Fisher Exact**

---
Uji Fisher's Exact Test merupakan uji signifikansi statistik yang digunakan untuk menentukan apakah terdapat hubungan atau asosiasi yang tidak terjadi secara acak antara dua variabel kategorikal yang disajikan dalam tabel kontingensi $2 \times 2$.

Berbeda dengan Uji Chi-Square yang menggunakan pendekatan aproksimasi (asimtotik), Fisher's Exact Test menghitung nilai probabilitas secara eksak, sehingga memberikan hasil yang lebih akurat pada kondisi tertentu. Uji ini sangat direkomendasikan dan menjadi pilihan utama ketika:

*   Ukuran sampel total relatif kecil ($N < 40$).
*   Terdapat sel pada tabel kontingensi yang memiliki frekuensi harapan (expected frequency) kurang dari 5.

---
**Rumus Uji Kebolehan**

---
Uji Fisher's Exact Test menggunakan distribusi hipergeometrik untuk menghitung probabilitas eksak dari susunan data yang diamati dengan asumsi bahwa jumlah total pada setiap baris dan kolom (marginal totals) bersifat tetap (fixed). Dengan pendekatan ini, probabilitas setiap kemungkinan tabel kontingensi yang memiliki total baris dan kolom yang sama dapat dihitung secara tepat tanpa menggunakan pendekatan aproksimasi.

Misalkan terdapat tabel kontingensi $2 \times 2$ sebagai berikut:

| | Kolom 1 | Kolom 2 | Total Baris |
| :--- | :---: | :---: | :---: |
| **Baris 1** | $a$ | $b$ | $a + b$ |
| **Baris 2** | $c$ | $d$ | $c + d$ |
| **Total Kolom** | $a + c$ | $b + d$ | $n$ (Total Sampel) |

Rumus untuk menghitung probabilitas eksak ($p$) dari kombinasi angka tersebut adalah:

$$p = \frac{(a+b)! (c+d)! (a+c)! (b+d)!}{a! b! c! d! n!}$$

> **Catatan:** Simbol `!` menunjukkan nilai Faktorial (contoh: $4! = 4 \times 3 \times 2 \times 1 = 24$).







"""

data = [
    [30, 20],
    [15, 35]
]

a = data[0][0]
b = data[0][1]
c = data[1][0]
d = data[1][1]

baris1 = a + b
baris2 = c + d

kolom1 = a + c
kolom2 = b + d

n = a + b + c + d

def faktorial(x):
    hasil = 1

    for i in range(1, x + 1):
        hasil *= i

    return hasil

p = (
    faktorial(baris1)
    * faktorial(baris2)
    * faktorial(kolom1)
    * faktorial(kolom2)
) / (
    faktorial(a)
    * faktorial(b)
    * faktorial(c)
    * faktorial(d)
    * faktorial(n)
)

print("a =", a)
print("b =", b)
print("c =", c)
print("d =", d)

print("Total Baris 1 =", baris1)
print("Total Baris 2 =", baris2)

print("Total Kolom 1 =", kolom1)
print("Total Kolom 2 =", kolom2)

print("Grand Total =", n)

print("Peluang Fisher =", p)

