"""**Uji Levene**

---
Uji Levene adalah uji statistik yang digunakan untuk mengetahui apakah varians dari dua atau lebih kelompok data bersifat homogen atau sama. Pada uji ini, H₀ menyatakan bahwa varians data homogen, sedangkan H₁ menyatakan varians data tidak homogen. Uji Levene lebih baik digunakan ketika data tidak berdistribusi normal.


---
1. Rata-rata Kelompok ($\bar{Y}_i$) atau Median Kelompok ($\tilde{Y}_i$):
   Uji Levene standar menggunakan nilai rata-rata kelompok ($i$).
   $$\bar{Y}_i = \frac{\sum_{j=1}^{n_i} Y_{ij}}{n_i}$$

2. Transformasi Data ke Nilai Deviasi Absolut ($Z_{ij}$):
   Menghitung seberapa jauh setiap data ($j$) menyimpang dari rata-rata kelompok ($i$) dalam nilai mutlak (selalu positif).
   $$Z_{ij} = |Y_{ij} - \bar{Y}_i|$$

3. Statistik Uji Levene ($W$ atau $F\text{-hitung}$):
   Menggunakan rumus Analysis of Variance (ANOVA) satu arah yang diterapkan pada data baru ($Z_{ij}$):
   $$W = \frac{(N - k)}{(k - 1)} \cdot \frac{\sum_{i=1}^{k} n_i (\bar{Z}_{i\cdot} - \bar{Z}_{\cdot\cdot})^2}{\sum_{i=1}^{k} \sum_{j=1}^{n_i} (Z_{ij} - \bar{Z}_{i\cdot})^2}$$

   *Keterangan:*
   * $k$ = Jumlah kelompok (pada kasus ini $k = 2$, yaitu data1 dan data2).
   * $N$ = Total seluruh data dari semua kelompok ($n_1 + n_2 = 5 + 5 = 10$).
   * $n_i$ = Jumlah data pada kelompok ke-$i$.
   * $\bar{Z}_{i\cdot}$ = Rata-rata nilai $Z$ pada kelompok ke-$i$.
   * $\bar{Z}_{\cdot\cdot}$ = Rata-rata nilai $Z$ keseluruhan secara total.

---



"""

data1 = [70, 75, 80, 72, 78]
data2 = [65, 85, 60, 90, 70]

# Jumlah data
